function firstNumber(value: string) {
  const match = value.replace(/\s+/g, "").match(/\d+/);
  return match ? Number(match[0]) : 0;
}

function closestAmountIcon(label: string, options: readonly (readonly [number, string])[]) {
  const amount = firstNumber(label);
  if (!amount) return options[0]?.[1] || "";

  return options.reduce((best, current) => {
    const bestDistance = Math.abs(amount - best[0]);
    const currentDistance = Math.abs(amount - current[0]);
    return currentDistance < bestDistance ? current : best;
  }, options[0])?.[1] || "";
}

function hasAny(value: string, terms: string[]) {
  return terms.some((term) => value.includes(term));
}

export function realOfferIcon(productSlug: string, label: string) {
  const normalized = label.toLocaleLowerCase("ru");

  if (productSlug === "roblox") {
    return closestAmountIcon(label, ROBUX_IMAGES);
  }

  if (productSlug === "pubg-mobile") {
    if (normalized.includes("uc")) return closestAmountIcon(label, PUBG_UC_IMAGES);
    return REAL_OFFER_IMAGES.pubgMobile;
  }

  if (productSlug === "oxide") {
    if (normalized.includes("elite")) return REAL_OFFER_IMAGES.oxideElitePass;
    if (hasAny(normalized, ["battle pass", "\u0431\u043e\u0435\u0432\u043e\u0439 \u043f\u0440\u043e\u043f\u0443\u0441\u043a"])) {
      return REAL_OFFER_IMAGES.oxideBattlePass;
    }
    if (hasAny(normalized, ["premium", "\u043f\u0440\u0435\u043c\u0438\u0443\u043c", "\u043f\u043e\u0434\u043f\u0438\u0441"])) {
      return REAL_OFFER_IMAGES.oxidePremium;
    }
    if (hasAny(normalized, ["ticket", "\u0431\u0438\u043b\u0435\u0442"])) return REAL_OFFER_IMAGES.oxideTicket;
    return closestAmountIcon(label, OXIDE_COIN_IMAGES);
  }

  if (productSlug === "playstation") {
    return REAL_OFFER_IMAGES.playstationTurkey;
  }

  return SLUG_IMAGES[productSlug] || "";
}
